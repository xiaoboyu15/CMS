package entity;

public class QA {
	private int QAPersonID;
	private String QAPersonName;
	private String QAName;
	private int UserID;
	public int getQAPersonID() {
		return QAPersonID;
	}
	public void setQAPersonID(int qAPersonID) {
		QAPersonID = qAPersonID;
	}
	public String getQAPersonName() {
		return QAPersonName;
	}
	public void setQAPersonName(String qAPersonName) {
		QAPersonName = qAPersonName;
	}
	public String getQAName() {
		return QAName;
	}
	public void setQAName(String qAName) {
		QAName = qAName;
	}
	public int getUserID() {
		return UserID;
	}
	public void setUserID(int userID) {
		UserID = userID;
	}
}
